"""
Tipo None

O Tipo de dado none em Python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo de dado sem tipo é mais apropriado.

OBS: O tipo de None é SEMPRE especificado com a primeira letra maiuscula

Certo : None
Errado: none

Quando Utilizamos?

Podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo,
antes de recebermos um valor final.
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))
